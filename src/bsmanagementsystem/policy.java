   private void fillcombo()
    {
        con = DBconnect.connect();
     try
        {
            String sql = "SELECT policytype FROM policytypes";
            pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            while (rs.next())
            {
                String name =rs.getString("policytype");
                jComboBox2.addItem(name);
            }
            
        }
        
        catch(SQLException e)
                {
                    System.out.println(e);
                }
     
    }
     
   public void tableLoad(){

         con = DBconnect.connect();
         try
        {
            
            String sql;
            sql = "SELECT policyID,policyType,policyInitialDate,rate,percentage,description,policyUpdateDate FROM policy_update";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            //JOptionPane.showMessageDialog(null,"Successfull");
        
        }
        
    catch(SQLException e)
                {
                    System.out.println(e);
                    JOptionPane.showMessageDialog(null,"UN-Successfull");
                }
        
    }
    
